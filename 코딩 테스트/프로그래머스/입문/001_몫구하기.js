// 라는 생각을 했었는데, 나머지 때문이 맞았었다.
// 처음에는 흔히 사용하고 알고있는 parseInt를 사용하면 되려나 생각해봤지만, 내가 풀려고 하는 문제와는 다른 방향인 거 같아
// 검색을 해보았는데 Math.floor()라는 메서드가 있었다. 정말 생소했지만 지금 풀어가는 과정에서 사고하게 되고,
// 적재적소에 필요한 것을 검색하고 몰랐었던 지식이 늘어가는 게 뿌듯한 것 같다.
