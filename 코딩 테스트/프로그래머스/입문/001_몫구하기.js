// 문제: 두 수의 나눗셈 몫 구하기
// 플랫폼: 프로그래머스 (Lv0)
// 제한사항: 0 < num1, num2 <= 100
// 입출력 예: (10,5) -> 2 / (7,2) -> 3

//
//  접근 방식
//  - 단순 나눗셈은 JS에서 소수까지 나오므로 그대로 쓰면 틀림
//  - '몫'만 필요하므로 Math.floor() 사용
//  - JS는 정수 나눗셈 연산자가 없다는 점 유의
// 

function solution(num1, num2) {
    return Math.floor(num1 / num2);
}

